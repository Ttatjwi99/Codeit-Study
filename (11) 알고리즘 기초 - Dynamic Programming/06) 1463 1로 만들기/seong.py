# 점화식: D[N] = min(D[N]-1, D[N]//2, D[N]//3) +1
N = int(input())
D = [0] * (N + 1)  # 최소 경우의 수가 담길 변수  # 편의를 위해 +1

for i in range(2, N + 1):  # i번 돌며 N을 구하는 최소의 연산을 구함
    
    D[i] = D[i - 1] + 1  # N-1 로 최소의 연산을 하는 경우
    
    # 6처럼 2와 3 모두로 나눠지는 경우도 있으므로 if, elif 사용 X.
    if i % 2 == 0:   # N % 2 == 0으로 최소의 연산을 하는 경우
        D[i] = min(D[i], D[i // 2] + 1)
    
    if i % 3 == 0:   # N % 3 == 0으로 최소의 연산을 하는 경우
        D[i] = min(D[i], D[i // 3] + 1)
        
print(D[N])
